class Resident {
  String? id;
  String? residentName;
  String? property;
  String? phone;
  String? age;
  String? sex;
  String? admissionForm;
  String? admissionDate;
  bool? statusActive;

  Resident({
    required this.id,
    required this.residentName,
    required this.property,
    required this.phone,
    required this.age,
    required this.sex,
    required this.statusActive,
    required this.admissionDate,
    required this.admissionForm,
  });
}
